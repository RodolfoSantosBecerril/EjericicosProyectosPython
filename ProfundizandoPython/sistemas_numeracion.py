#valores decimal
a=10
print(f'valor a:{a}')
#valores en sistema binario
a=0b1010
print(f'valor a:{a}')
#octal
a=0o12
print(f'valor a:{a}')
#hexadecimal
a=0xA
print(f'valor a:{a}')

#Convertir tipo entero pero incluyendo la base
a=int('23',10)
print(f'valor a:{a}')
#Base binario
a=int('10111',2) #para binario la base es 2
print(f'valor a:{a}')
#Base octal
a=int('27',8)
print(f'valor a:{a}')
#base Hexadecimal
a=int('17',16)
print(f'valor a:{a}')
#se puede usar cualquier base que sea valido
a=int('1234',5)
print(f'valor a:{a}')

